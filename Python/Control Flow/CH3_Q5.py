x = int(input("enter num: "))
op = input("enter op: ")
y = int(input("enter num: "))

if op == "+":
    print (x, "+", y, "=", x+y)

elif op == "*" or op == "×":
    print (x, "×", y, "=", x*y)

elif op == "-":
    print (x, "-", y, "=", x-y)

elif op == "/" or op == "÷":
    print (x, "÷", y, "=", x/y)